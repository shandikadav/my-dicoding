let age = 20;

// binary operator
const penjumlahan = 5 + 5;
const pengurangan = 5 - 5;
const perkalian = 5 * 5;
const pembagian = 5 / 5;
const modulus = 5 % 5;

console.log(penjumlahan); // 10
console.log(pengurangan); // 0
console.log(perkalian); // 25
console.log(pembagian); // 1
console.log(modulus); // 0

// unary operator
console.log(typeof age); // number

//ternary operator
age < 18 ? console.log("Remaja") : console.log("Dewasa"); // Output: Dewasa
